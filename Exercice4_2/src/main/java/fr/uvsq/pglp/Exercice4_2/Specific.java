package fr.uvsq.pglp.Exercice4_2;

public class Specific {
	
	public double plus(double a, double b) {
		return a+b;
		
	}
	
	public double moins(double a, double b) {
		return a-b;
	}
	
	public double mult(double a, double b) {
		return a*b;
	}
	
	public double div(double a, double b) {
		return a/b;
	}

}
